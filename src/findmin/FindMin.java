
package findmin;


public class FindMin {

    
    public static void main(String[] args) {
        
        
       
findMin(3, 56, 6, 99, 67);
}

    private static void findMin(int a, int b, int c, int d, int e) {

    
int min = a;
if (b < min) {
min=b;
}
if (c < min) {
min = c;
}
if (d < min) {
min = d;
}
if (e < min) {
min = e;
}


System.out.println(min);
}

    }


    
    

